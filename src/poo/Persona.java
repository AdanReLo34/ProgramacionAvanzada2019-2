package poo;

/**
 *
 * @author Roberto Cruz Leija
 */
public class Persona {
    
    private int edad;
    private String nombre;
    private double peso;
    private double altura;
    private String genero;

    public Persona() {
        edad = 0;
        nombre = "";
        peso = 0;
        altura = 0;
        genero = "desconocido";
    }

    
    
    public Persona(int edad, String nombre, double peso, double altura, String genero) {
        this.edad = edad;
        this.nombre = nombre;
        this.peso = peso;
        this.altura = altura;
        this.genero = genero;
    }

    /**
     * @return the edad
     */
    public int getEdad() {
        return edad;
    }

    /**
     * @param edad the edad to set
     */
    public void setEdad(int edad) {
        this.edad = edad;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the peso
     */
    public double getPeso() {
        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(double peso) {
        this.peso = peso;
    }

    /**
     * @return the altura
     */
    public double getAltura() {
        return altura;
    }

    /**
     * @param altura the altura to set
     */
    public void setAltura(double altura) {
        this.altura = altura;
    }

    /**
     * @return the genero
     */
    public String getGenero() {
        return genero;
    }

    /**
     * @param genero the genero to set
     */
    public void setGenero(String genero) {
        this.genero = genero;
    }
    
    
    
    
}
